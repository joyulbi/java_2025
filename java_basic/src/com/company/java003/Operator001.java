package com.company.java003;

public class Operator001 {
	public static void main(String[]args) {
		//먼저() 값(+,-,*,/,%,++,--) 비교(>,<..) 조건 처리 후(&&,||,?:) 대입
		//1.값
		int a=10,b=3;
		System.out.println(a+b);
		System.out.println(a-b);
		System.out.println(a*b);
		System.out.println(a/b);
		System.out.println(a%b);
		
	} 
}
